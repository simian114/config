bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_ADJUST, _LOWER, _ARROW);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_ADJUST, _LOWER, _ARROW);
      }
      return false;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
      } else {
        layer_off(_RAISE);
      }
      return false;
    case TERM:
      if (record->event.pressed) {
        layer_on(_TERM);
      } else {
        layer_off(_TERM);
      }
      return false;
    case ADJUST:
        if (record->event.pressed) {
            layer_on(_ADJUST);
            update_tri_layer(_ADJUST, _LOWER, _ARROW);
        } else {
            layer_off(_ADJUST);
            update_tri_layer(_ADJUST, _LOWER, _ARROW);
        }
        return false;
  }
    return true;
}